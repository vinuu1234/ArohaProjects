package com.demo.aroha.weekend;

public class Pattern5 {

	public static void main(String[] args) {

		int n=5;
		int num=75;
		for (int i = 0; i <= n; i++) {
			for (int j = 0; j < i; j++) {
				//char ch=(char) (num);
				System.out.print(num +" ");
				num++;
				//75
				//76 77
				//78 79 80
				//81 82 83 84
				//85 86 87 88 89

			}
			System.out.println();
			
		}
	}

}
